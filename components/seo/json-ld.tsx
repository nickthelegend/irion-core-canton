import React from "react";

interface JsonLdProps {
  type: "organization" | "faq" | "breadcrumb";
  faqItems?: { question: string; answer: string }[];
  breadcrumbItems?: { name: string; item: string }[];
}

export default function JsonLd({ type, faqItems, breadcrumbItems }: JsonLdProps) {
  let data: any = null;

  if (type === "organization") {
    data = {
      "@context": "https://schema.org",
      "@graph": [
        {
          "@type": "FinancialService",
          "@id": "https://app.irion.finance/#organization",
          "name": "IRION Finance",
          "url": "https://app.irion.finance",
          "logo": "https://app.irion.finance/irion-logo.png",
          "image": "https://app.irion.finance/irion-logo.png",
          "description": "Private consumer credit on Stellar — BNPL, lending, and a credit score proven with a zero-knowledge proof.",
          "tagline": "Buy Now, Pay Never.",
          "feesAndCommissionsSpecification": "Auto-repaid from on-chain yield generated by collateral assets.",
          "sameAs": [
            "https://x.com/IrionFinance"
          ]
        },
        {
          "@type": "WebSite",
          "@id": "https://app.irion.finance/#website",
          "url": "https://app.irion.finance",
          "name": "IRION Finance",
          "description": "Private consumer credit on Stellar: BNPL, lend/borrow, and a zero-knowledge credit score.",
          "publisher": {
            "@id": "https://app.irion.finance/#organization"
          }
        }
      ]
    };
  } else if (type === "faq") {
    const items = faqItems || [
      {
        question: "What is Buy Now, Pay Never?",
        answer: "Buy Now, Pay Never (BNPL) allows users to purchase items on credit by locking USDC collateral. The collateral is automatically routed to yield-generating protocols (such as lending pools or liquidity pools on Stellar), and the earned yield pays off the outstanding credit over time."
      },
      {
        question: "How does the zero-knowledge credit score work?",
        answer: "IRION Finance proves your credit score with a zero-knowledge proof. It processes off-chain financial data confidentially, issuing an on-chain cryptographic attestation. Your raw financial data never leaves your device."
      },
      {
        question: "Is my financial data private?",
        answer: "Yes. Because the score is proven with a zero-knowledge proof, no one—not even the IRION protocol developers—can inspect your private financial data. Only the final score is attested on-chain."
      },
      {
        question: "What is IRION Finance built on?",
        answer: "IRION Finance is built natively on the high-performance Stellar blockchain, leveraging its low latency, high throughput, and secure smart contracts to handle collateral management, lending pools, and BNPL checkouts."
      }
    ];
    data = {
      "@context": "https://schema.org",
      "@type": "FAQPage",
      "mainEntity": items.map((item) => ({
        "@type": "Question",
        "name": item.question,
        "acceptedAnswer": {
          "@type": "Answer",
          "text": item.answer
        }
      }))
    };
  } else if (type === "breadcrumb") {
    data = {
      "@context": "https://schema.org",
      "@type": "BreadcrumbList",
      "itemListElement": (breadcrumbItems || []).map((item, index) => ({
        "@type": "ListItem",
        "position": index + 1,
        "name": item.name,
        "item": item.item
      }))
    };
  }

  if (!data) return null;

  return (
    <script
      type="application/ld+json"
      dangerouslySetInnerHTML={{ __html: JSON.stringify(data) }}
    />
  );
}
